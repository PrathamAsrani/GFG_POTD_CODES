class Solution {
  public:
    long long sumMatrix(long long n, long long q) {
        long long low = 2, high = 2*n;
        if(q < low || q > high) return 0;
        return min(q-low, high-q) + 1;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        long long n,q;
        
        cin>>n>>q;

        Solution ob;
        cout << ob.sumMatrix(n,q) << endl;
    }
    return 0;
}
