// User function template for C++
class Solution {
  public:

    string removeDups(string str) {
        vector<int> dp(26, 0);
        for(char &c: str)
            dp[c-'a']++;
        string ans;
        for(char c: str){
            if(dp[c-'a'] > 0){
                dp[c-'a'] = 0;
                ans += c;
            }
        }
        return ans;
    }
};

