class Solution {
  public:
    void nearlySorted(vector<int>& arr, int k) {
        // code
        int i = 1;
        while(i < arr.size()){
            int j = i-1, key = arr[i];
            while(j >= max(0, i-k) && arr[j] > key){
                arr[j+1] = arr[j];
                j--;
            }
            arr[j+1] = key;
            i++;
        }
        // tc: O
    }
};

