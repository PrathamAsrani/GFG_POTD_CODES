class Solution {
  public:
    int findMaxSum(vector<int>& arr) {
        // code here
        int n = arr.size();
        int one = 0, two = 0;
        for(int i = 0; i < n; i++){
            int tmp = max(one, two + arr[i]);
            two = one;
            one = tmp;
        }
        return max(one, two);
    }
};


